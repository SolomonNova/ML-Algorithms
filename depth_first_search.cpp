int main()
{
    std::vector<std::vector<int>> graph = 
    {
        {1, 2, 4},
        {0, 2},
        {0, 1},
        {4},
        {0}
    };

    std::vector<state> states
    {
        {0},
        {1},
        {2},
        {3},
        {4}
    };

    dfs(graph, states);
    print_nodes_info(states);

    return 0;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/*========================================= Function Definitions =====================================================*/
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/////////////////////////////////////////////////////////////////////////////////////
void dfs(std::vector<std::vector<int>> &graph, std::vector<state> &states)
{
    for (size_t x = 0; x < states.size(); x++)
    {
        states[x].color = 'W';
        states[x].parent = -1;
        states[x].depth = 0;
    }

    size_t time = 0;
    
    for (size_t x = 0; x < states.size(); x++)
        if (states[x].color == 'W')
        {
            if (reachedGoal) return;
            dfs_visit(graph, states, states[x], time);
        }

    std::cout<< "dfs finihsed\n";
}

/////////////////////////////////////////////////////////////////////////////////////
void dfs_visit
(
    std::vector<std::vector<int>> &graph,
    std::vector<state> &states,
    state &u,
    size_t &time
)
{
    if (reachedGoal) return;
    
    time++;
    u.color = 'G';
    u.discoveryTime = time;


    if (is_goal(u))
    {
        std::cout << "goal reached\n";
        reachedGoal = true;
        return;
    }
    
    for (int v : graph[u.value])
    {
        if (states[v].color == 'W')
        {
            states[v].parent = u.value;
            states[v].depth = u.depth + 1;
            dfs_visit(graph, states, states[v], time);
        }
    }

    u.color = 'B';
    time++;
    u.finishTime = time; 
}


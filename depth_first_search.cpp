#include <iostream>
#include <cstddef>  // for size_t
#include <vector>   // for std::vector<>

struct state
{
    size_t value;
    size_t discoveryTime;
    size_t finishTime;
    int    parent;
    int    depth;
    char   color;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/*========================================= Helper Functions ====================================================*/
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/////////////////////////////////////////////////////////////////////////////////////

bool reachedGoal = false;
inline bool is_goal(state s) { return s.value == 3; }

////////////////////////////////////////////////////////////////////////////////////
void print_nodes_info(std::vector<state> &states)
{
    for (state s : states)
    {
        std::cout << "state:          " << s.value         << "\n";
        std::cout << "parent:         " << s.parent        << "\n";
        std::cout << "depth:          " << s.depth         << "\n";
        std::cout << "discovery time: " << s.discoveryTime << "\n";
        std::cout << "finish time:    " << s.finishTime    << "\n";
        std::cout << "color:          " << s.color         << "\n";
        std::cout << "\n\n";
      }
}

